#!/usr/bin/env python3
"""run_e2e_loop.py — E2E 자율 루프 통합 진입점.

전 조직 봇이 일일회고/주간회의 채팅에 참여해 보고·조치사항을
GoalTracker에 자동 등록하는 end-to-end 시나리오를 실행한다.

사용 예:
    # 일일회고 시나리오 실행 (dry-run)
    ./.venv/bin/python run_e2e_loop.py --type daily_retro --dry-run

    # 주간회의 시나리오 실행 (실제 GoalTracker 연동)
    ENABLE_GOAL_TRACKER=1 ./.venv/bin/python run_e2e_loop.py --type weekly_meeting

    # 커스텀 보고서 파일 지정
    ./.venv/bin/python run_e2e_loop.py --type daily_retro --report docs/retros/2026-03-25.md

실행 흐름:
    1. 환경 설정 로드 (.env / ~/.ai-org/config.yaml)
    2. GoalTrackerClient 초기화 (ENABLE_GOAL_TRACKER에 따라 tracker 주입 여부 결정)
    3. MultibotMeetingHandler 초기화
    4. handle() 실행 — 봇 보고 수집 → GoalTracker 등록 → 루프 실행
    5. 결과 출력 (states_visited, dispatched_count)
"""
from __future__ import annotations

import argparse
import asyncio
import os
import sys
from datetime import datetime, timezone
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent
sys.path.insert(0, str(PROJECT_ROOT))


# ── 환경 로드 ─────────────────────────────────────────────────────────────────

def _load_env() -> None:
    for env_path in (Path.home() / ".ai-org" / "config.yaml", PROJECT_ROOT / ".env"):
        if not env_path.exists():
            continue
        for line in env_path.read_text().splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, _, v = line.partition("=")
                os.environ.setdefault(k.strip(), v.strip())


_load_env()

ENABLE_GOAL_TRACKER = os.environ.get("ENABLE_GOAL_TRACKER", "0") == "1"
GROUP_CHAT_ID = int(os.environ.get("TELEGRAM_GROUP_CHAT_ID", "0"))
PM_BOT_TOKEN = os.environ.get("PM_BOT_TOKEN", "")


# ── 샘플 보고서 ───────────────────────────────────────────────────────────────

