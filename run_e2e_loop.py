# ── 전송 함수 ─────────────────────────────────────────────────────────────────

async def _make_send_func():
    """Telegram 전송 함수 생성 (봇 토큰 있으면 실제, 없으면 noop)."""
    if not PM_BOT_TOKEN:
        async def _noop_send(chat_id: int, text: str) -> None:
            print(f"[SEND → {chat_id}] {text[:80]}")
        return _noop_send

    try:
        from telegram import Bot

        from core.telegram_formatting import markdown_to_html

        async def _real_send(chat_id: int, text: str) -> None:
            try:
                async with Bot(token=PM_BOT_TOKEN) as b:
                    await b.send_message(
                        chat_id=chat_id,
                        text=markdown_to_html(text),
                        parse_mode="HTML",
                    )
            except Exception as e:
                print(f"[SEND 실패] {e}")

        return _real_send
    except ImportError:
        async def _fallback_send(chat_id: int, text: str) -> None:
            print(f"[SEND → {chat_id}] {text[:80]}")
        return _fallback_send


# ── GoalTracker 초기화 ────────────────────────────────────────────────────────

async def _make_goal_tracker():
    """GoalTracker 인스턴스 생성 (ENABLE_GOAL_TRACKER=1 필요)."""
    if not ENABLE_GOAL_TRACKER:
        print("[E2E] ENABLE_GOAL_TRACKER=0 — dry-run 모드")
        return None

    try:
        from pathlib import Path as _Path

        from core.claim_manager import ClaimManager
        from core.context_db import ContextDB
        from core.goal_tracker import GoalTracker
        from core.memory_manager import MemoryManager
        from core.pm_orchestrator import PMOrchestrator
        from core.task_graph import TaskGraph

        db_path = _Path(os.environ.get("CONTEXT_DB_PATH", "~/.ai-org/context.db")).expanduser()
        db = ContextDB(db_path)
        await db.initialize()

        send_func = await _make_send_func()

        async def _telegram_send(chat_id: int, text: str, **kwargs) -> None:
            await send_func(chat_id, text)

        orch = PMOrchestrator(
            context_db=db,
            task_graph=TaskGraph(db),
            claim_manager=ClaimManager(),
            memory=MemoryManager("pm"),
            org_id="pm",
            telegram_send_func=_telegram_send,
        )
        tracker = GoalTracker(
            context_db=db,
            orchestrator=orch,
            telegram_send_func=_telegram_send,
            org_id="pm",
        )
        print("[E2E] GoalTracker 초기화 완료")
        return tracker
    except Exception as e:
        print(f"[E2E] GoalTracker 초기화 실패 (dry-run 전환): {e}")
        return None


# ── 봇 보고 요청 함수 ─────────────────────────────────────────────────────────

async def _simulate_bot_report(org_id: str, chat_id: int, meeting_type: str) -> str:
    """E2E 테스트용 봇 보고 시뮬레이션 (실제 봇 없이 고정 응답)."""
    _reports = {
        "aiorg_engineering_bot": "개발실 보고: 자율 루프 구현 완료. 다음 주 E2E 테스트 완성 예정.",
        "aiorg_ops_bot":         "운영실 보고: 시스템 안정. 배포 파이프라인 정상.",
        "aiorg_design_bot":      "디자인실 보고: UI 컴포넌트 검토 완료.",
        "aiorg_product_bot":     "기획실 보고: Q2 PRD 초안 작성 중.",
        "aiorg_growth_bot":      "성장실 보고: MAU +12%. 주요 지표 양호.",
        "aiorg_research_bot":    "리서치실 보고: 경쟁사 분석 3건 완료.",
    }
    await asyncio.sleep(0.1)  # 실제 봇 응답 지연 시뮬레이션
    return _reports.get(org_id, f"[{org_id}] 보고 내용 없음")


# ── 메인 실행 ─────────────────────────────────────────────────────────────────

async def run_e2e_scenario(
    meeting_type: str,
    report_file: str | None,
    dry_run: bool,
    chat_id: int,
) -> None:
    """E2E 자율 루프 시나리오 실행."""
    from goal_tracker.goal_tracker_client import GoalTrackerClient
    from goal_tracker.multibot_meeting_handler import MultibotMeetingHandler

    now = datetime.now(timezone.utc)
    date_str = now.strftime("%Y-%m-%d")
    print(f"\n{'='*60}")
    print("  E2E 자율 루프 시나리오")
    print(f"  유형: {meeting_type}")
    print(f"  날짜: {date_str}")
    print(f"  dry_run: {dry_run or not ENABLE_GOAL_TRACKER}")
    print(f"{'='*60}\n")

    # 보고서 준비
    if report_file and Path(report_file).exists():
        report_text = Path(report_file).read_text(encoding="utf-8")
        print(f"[E2E] 보고서 로드: {report_file} ({len(report_text)}자)")
    elif meeting_type == "weekly_meeting":
        report_text = _SAMPLE_WEEKLY_MEETING.format(date=date_str, now=now.isoformat())
        print("[E2E] 샘플 주간회의 보고서 사용")
    else:
        report_text = _SAMPLE_DAILY_RETRO.format(date=date_str, now=now.isoformat())
        print("[E2E] 샘플 일일회고 보고서 사용")

    # 컴포넌트 초기화
    tracker = None if dry_run else await _make_goal_tracker()
    send_func = await _make_send_func()

    client = GoalTrackerClient(
        goal_tracker=tracker,
        org_id="aiorg_pm_bot",
        chat_id=chat_id or GROUP_CHAT_ID,
        send_func=send_func,
        dry_run=(tracker is None),
    )

    handler = MultibotMeetingHandler(
        client=client,
        send_func=send_func,
        request_bot_report=_simulate_bot_report,
        bot_request_interval=0.5,   # E2E 테스트에서는 짧은 인터벌
        collect_timeout=30.0,
    )

    # 시나리오 실행
    trigger_messages = {
        "daily_retro": "일일회고 시작합니다",
        "weekly_meeting": "주간회의를 시작하겠습니다",
    }
    trigger = trigger_messages.get(meeting_type, meeting_type)

    print(f"[E2E] 핸들러 실행 중... (트리거: '{trigger}')")
    result = await handler.handle(
        message_text=trigger,
        chat_id=chat_id or GROUP_CHAT_ID,
        force=True,  # E2E 실행 시 항상 강제 실행
    )

    # 결과 출력
    print(f"\n{'─'*60}")
    print("  E2E 결과 요약")
    print(f"{'─'*60}")
    print(f"  성공: {result.success}")
    print(f"  회의 유형: {result.meeting_type}")
    print(f"  봇 보고 수: {result.report_count}")
    print(f"  등록된 조치사항: {result.registered_count}")

    if result.register_result:
        rr = result.register_result
        print(f"  파싱된 아이템: {rr.action_items_found}")
        print(f"  루프 상태: {' → '.join(rr.loop_states) if rr.loop_states else 'N/A'}")
        print(f"  배분 완료: {rr.dispatched_count}")

    if result.bot_reports:
        print("\n  봇 보고 상세:")
        for rep in result.bot_reports:
            status = "✅" if rep.success else "❌"
            print(f"    {status} {rep.org_name}: {rep.report_text[:60]}...")

    if result.error:
        print(f"\n  오류: {result.error}")

    print(f"\n[E2E] 완료 — {datetime.now(timezone.utc).isoformat()}")


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="E2E 자율 루프 — 전 조직 봇 일일회고/주간회의 GoalTracker 연동 검증"
    )
    parser.add_argument(
        "--type", choices=["daily_retro", "weekly_meeting"],
        default="daily_retro",
        help="회의 유형 (기본: daily_retro)",
    )
    parser.add_argument(
        "--report", type=str, default=None,
        help="보고서 파일 경로 (없으면 샘플 사용)",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="실제 GoalTracker 연동 없이 로그만 출력",
    )
    parser.add_argument(
        "--chat-id", type=int, default=0,
        help="Telegram 채팅방 ID (기본: TELEGRAM_GROUP_CHAT_ID 환경변수)",
    )
    return parser.parse_args()


if __name__ == "__main__":
    args = _parse_args()
    asyncio.run(run_e2e_scenario(
        meeting_type=args.type,
        report_file=args.report,
        dry_run=args.dry_run,
        chat_id=args.chat_id,
    ))
