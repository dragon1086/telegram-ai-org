class TestScenario2EvaluateToReplan:
    """시나리오 2: evaluate → replan 전이 검증."""

    def test_evaluate_not_achieved_triggers_replan(self):
        """미달성 시 EVALUATE → REPLAN 전이."""
        sm = GoalTrackerStateMachine("G-test-002")
        sm.start_evaluate()  # → EVALUATE

        eval_result = EvaluationResult(
            achieved=False,
            progress_pct=0.3,
            done_count=1,
            total_count=3,
            remaining_work="2개 태스크 미완료",
        )
        transitioned = sm.start_replan(eval_result)
        assert transitioned is True
        assert sm.state == GoalTrackerState.REPLAN

    def test_evaluate_achieved_returns_to_idle(self):
        """달성 시 EVALUATE → IDLE 복귀."""
        sm = GoalTrackerStateMachine("G-test-002")
        sm.start_evaluate()  # → EVALUATE

        eval_achieved = EvaluationResult(
            achieved=True,
            progress_pct=1.0,
            done_count=3,
            total_count=3,
            confidence=0.95,
        )
        transitioned = sm.start_replan(eval_achieved)
        assert transitioned is False  # 달성됨 → REPLAN 진입 불가
        # IDLE 복귀
        sm.return_to_idle("달성")
        assert sm.state == GoalTrackerState.IDLE

    @pytest.mark.asyncio
    async def test_runner_evaluate_triggers_replan_when_tasks_exist(self):
        """태스크 있을 때 루프 runner가 replan 단계까지 실행."""
        runner = AutonomousLoopRunner(goal_id="G-loop-002")
        result = await runner.run_cycle(registered_ids=["G-pm-001", "G-pm-002"])
        assert "replan" in result.states_visited
        assert len(result.task_ids) == 2

    @pytest.mark.asyncio
    async def test_runner_skips_replan_when_no_tasks(self):
        """태스크 없을 때 루프 runner가 즉시 idle 복귀."""
        runner = AutonomousLoopRunner(goal_id="G-loop-002-empty")
        result = await runner.run_cycle(registered_ids=[])  # 빈 목록
        # achieved=True (아무것도 할 일 없음) → replan 건너뜀
        assert "replan" not in result.states_visited
        assert result.dispatched is False


