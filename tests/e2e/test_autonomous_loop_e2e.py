class TestScenario1IdleTrigger:
    """시나리오 1: idle 상태에서 루프 트리거 — AutonomousLoopRunner."""

    def test_state_machine_starts_in_idle(self):
        """상태머신 초기 상태 확인 — IDLE이어야 함."""
        sm = GoalTrackerStateMachine("G-test-001")
        assert sm.state == GoalTrackerState.IDLE, "초기 상태는 IDLE이어야 함"

    def test_start_evaluate_transitions_to_evaluate(self):
        """IDLE → EVALUATE 전이 성공."""
        sm = GoalTrackerStateMachine("G-test-001")
        result = sm.start_evaluate()
        assert result is True
        assert sm.state == GoalTrackerState.EVALUATE

    def test_cannot_start_evaluate_if_max_iterations_reached(self):
        """최대 반복 도달 시 EVALUATE 진입 불가."""
        sm = GoalTrackerStateMachine("G-test-001", max_iterations=2)
        sm.ctx.iteration = 2  # 최대 반복 도달
        result = sm.start_evaluate()
        assert result is False
        assert sm.state == GoalTrackerState.IDLE

    @pytest.mark.asyncio
    async def test_loop_runner_starts_from_idle(self):
        """AutonomousLoopRunner: idle에서 시작하여 evaluate로 진입."""
        sm = GoalTrackerStateMachine("G-loop-001")
        runner = AutonomousLoopRunner(goal_id="G-loop-001", state_machine=sm)

        assert runner.current_state == GoalTrackerState.IDLE
        result = await runner.run_cycle(registered_ids=["G-pm-001"])
        assert "idle" in result.states_visited
        assert "evaluate" in result.states_visited


